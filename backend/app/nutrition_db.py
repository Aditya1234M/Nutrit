def lookup_food(food_name: str) -> dict | None:
    """Lookup nutrition data for a food item. Returns per-100g values."""
    # Normalize the name: lowercase, replace spaces with underscores
    normalized = food_name.lower().strip().replace(" ", "_").replace("-", "_")

    data = NUTRITION_DATA.get(normalized)
    if data:
        cal, pro, carb, fat, fiber = data
        return {
            "food_name": food_name,
            "calories_per_100g": cal,
            "protein_per_100g": pro,
            "carbs_per_100g": carb,
            "fat_per_100g": fat,
            "fiber_per_100g": fiber,
            "serving_size_g": 100.0,
        }

    # Fuzzy fallback — try partial match
    for key, data in NUTRITION_DATA.items():
        if normalized in key or key in normalized:
            cal, pro, carb, fat, fiber = data
            return {
                "food_name": key.replace("_", " ").title(),
                "calories_per_100g": cal,
                "protein_per_100g": pro,
                "carbs_per_100g": carb,
                "fat_per_100g": fat,
                "fiber_per_100g": fiber,
                "serving_size_g": 100.0,
            }

    return None


def get_all_food_names() -> list[str]:
    """Return all known food names."""
    return [k.replace("_", " ").title() for k in NUTRITION_DATA.keys()]
